public class Diamond {
//----------------------------------------------------------------------------------------------------------------------------------------
    private Scanner keyboard = new Scanner(System.in);
    private long carat, value;
//----------------------------------------------------------------------------------------------------------------------------------------
    public Diamond(long carat, long value){

       this.carat = carat;
       this.value = value;

    }
//----------------------------------------------------------------------------------------------------------------------------------------
    public void readinput(){

       System.out.print("Enter carats and value    : ");
       carat = keyboard.nextLong();
       value = keyboard.nextLong();

    }
//----------------------------------------------------------------------------------------------------------------------------------------
    public long getCarat(){

       return carat;

    }
//----------------------------------------------------------------------------------------------------------------------------------------
    public long getValue(){

       return value;

    }
//----------------------------------------------------------------------------------------------------------------------------------------
}
